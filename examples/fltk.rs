fn main() -> Result<(), Box<dyn std::error::Error>> {
    let my_image = ez_pixmap::RgbaImage::from(PXM)?;
    let app = app::App::default();
    let mut win = window::OverlayWindow::default().with_size(50, 34);
    let mut frame = frame::Frame::default().size_of(&win);
    win.end();
    win.show();
    frame.draw(move || {
        draw::draw_image(my_image.data(), 0, 0, 50, 34, ColorDepth::Rgba8).unwrap();
    });
    app.run().unwrap();
    Ok(())
}
