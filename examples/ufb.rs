fn main() -> Result<(), Box<dyn std::error::Error>> {
    let my_image = ez_pixmap::RgbaImage::from(PXM)?;
    let mut win = ufb::Window::new(
        my_image.width(),
        my_image.height(),
        ufb::ColorDepth::Rgba8,
        "My Pixmap",
    )?;
    win.get_frame().copy_from_slice(my_image.data());
    win.show();
    Ok(())
}
